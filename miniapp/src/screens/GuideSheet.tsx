// Одна секция аккордеона. Высоту тела МЕРЯЕМ по контенту (ref) — иначе длинный
// раздел режется фикс-потолком max-height. При раскрытии — подскролл в вид.
function Sec({ s, i, open, onToggle }: {
  s: Section; i: number; open: boolean; onToggle: (i: number) => void
}) {
  const innerRef = useRef<HTMLDivElement>(null)
  const secRef = useRef<HTMLDivElement>(null)
  const [h, setH] = useState(0)
  useEffect(() => {
    if (!open) return
    const measure = () => setH(innerRef.current?.scrollHeight ?? 0)
    measure()
    const id = setTimeout(() => {          // после раскрытия — показать раздел целиком
      secRef.current?.scrollIntoView({ block: 'nearest', behavior: 'smooth' })
    }, 90)
    window.addEventListener('resize', measure)
    return () => { clearTimeout(id); window.removeEventListener('resize', measure) }
  }, [open])
  return (
    <div ref={secRef} className={`guide-sec${open ? ' open' : ''}`}
      style={{ ['--acc' as string]: s.acc }}>
      <button className="guide-q" onClick={() => onToggle(i)}>
        <span className="guide-ic">{s.emoji}</span>
        <span className="guide-t">{s.title}</span>
        <span className="guide-chev">{open ? '−' : '+'}</span>
      </button>
      <div className="guide-body" style={{ maxHeight: open ? h : 0 }}>
        <div className="guide-inner" ref={innerRef}>
          {s.body.map((p, k) => <p key={k}>{p}</p>)}
        </div>
      </div>
    </div>
  )
}

export default function GuideSheet({ onClose }: { onClose: () => void }) {
  const [open, setOpen] = useState<number | null>(0)
  const toggle = (i: number) => { haptic('light'); setOpen(open === i ? null : i) }
  return (
    <div className="sv-backdrop" onClick={onClose}>
      <div className="guide-sheet" onClick={(e) => e.stopPropagation()}>
        <div className="guide-top">
          <div className="guide-title"><span className="guide-book">📖</span> Как играть</div>
          <button className="guide-x" onClick={() => { haptic('light'); onClose() }} aria-label="Закрыть">✕</button>
        </div>
        <div className="guide-scroll">
          {SECTIONS.map((s, i) => (
            <Sec key={i} s={s} i={i} open={open === i} onToggle={toggle} />
          ))}
          <div className="guide-tail">Появились вопросы? Загляни в наш канал — там подскажут. 🍺</div>
        </div>
      </div>
    </div>
  )
}
