"""Подкидыш в общий чат: что-то «потерялось» — кто первый нажал, тот подобрал.

Исход роллится при подборе: ресурс (3–7 одного вида), пусто («показалось»),
или смешной хлам (не повезло). Тон — трактирный, как и весь Недоливск.
"""

import random

from bot.game import balance

# Зазывалки подкидыша (что бросается в глаза у порога)
FLAVORS = (
    "Эй! Что-то блестит в грязи у порога…",
    "Гляди — кто-то обронил узелок прямо на дороге!",
    "Под лавкой завалялось что-то. Чьё — непонятно.",
    "Ой, кошель валяется! Или не кошель… подними — узнаешь.",
    "В пыли что-то лежит. Авось добро, а авось…",
    "Смотри-ка, свёрток у колодца! Ничейный вроде.",
    "Кто-то выронил мешочек и не заметил. Хватай, пока не вернулся!",
)

# Хлам — «не повезло». Эмодзи + что подобрал (трактирный тон).
JUNK = (
    "💀 дохлую крысу",
    "🍆 чей-то резиновый уд, изрядно потёртый",
    "🎈 использованный презерватив",
    "🦷 чью-то вставную челюсть",
    "🧦 один драный носок (второй не нашёлся)",
    "🩲 застиранные чужие подштанники",
    "💩 кое-что тёплое и коричневое",
    "🐀 крысиный хвост без крысы",
    "🥾 ношеный лапоть, полный чего-то",
    "🤢 огрызок, обглоданный до тебя",
)


def roll(rng: random.Random | None = None) -> dict:
    """Что в подкидыше: {'kind': 'resource'|'nothing'|'junk', ...}."""
    rng = rng or random
    r = rng.random()
    if r < balance.LOOT_RESOURCE_CHANCE:
        res = rng.choice(balance.RESOURCES)
        qty = rng.randint(balance.LOOT_QTY_MIN, balance.LOOT_QTY_MAX)
        return {"kind": "resource", "res": res, "qty": qty}
    if r < balance.LOOT_RESOURCE_CHANCE + balance.LOOT_NOTHING_CHANCE:
        return {"kind": "nothing"}
    return {"kind": "junk", "junk": rng.choice(JUNK)}


def flavor(rng: random.Random | None = None) -> str:
    return (rng or random).choice(FLAVORS)
