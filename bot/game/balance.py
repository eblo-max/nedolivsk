MARKET_WHOLESALE_WEIGHT = 1.0
MARKET_P2P_WEIGHT = 0.3     # биржевая сделка двигает оптовую цену (мягко)


def market_factor(glut: float, scale: int = 1) -> float:
    """Множитель справедливой цены от перекоса рынка: завал → пол, дефицит →
    потолок. Пороги множатся на scale (число активных чатов) — адаптивно."""
    scale = max(1, int(scale))
    if glut > 0:
        drop = min(1.0, glut / (MARKET_SATURATION_PER_CHAT * scale))
        return round(1.0 - drop * (1.0 - MARKET_PRICE_FLOOR), 3)
    if glut < 0:
        up = min(1.0, -glut / (MARKET_SHORTAGE_PER_CHAT * scale))
        return round(1.0 + up * (MARKET_PRICE_CEIL - 1.0), 3)
    return 1.0


