def label(npc_id: str) -> str:
    c = CATALOG.get(npc_id)
    return f"{c.emoji} {c.name}" if c else npc_id


def by_estate(estate: str) -> list[Citizen]:
    return [c for c in CATALOG.values() if c.estate == estate]


def traders() -> list[Citizen]:
    """Горожане, что приходят покупать твой товар (есть архетип торга)."""
    return [c for c in CATALOG.values() if c.arch]


def random_trader(rng: random.Random | None = None) -> Citizen:
    rng = rng or random
    pool = traders()
    return rng.choices(pool, weights=[c.freq for c in pool], k=1)[0]


def pulsers() -> list[Citizen]:
    """Горожане, чьи дела двигают рынок (есть pulse)."""
    return [c for c in CATALOG.values() if c.pulse]


def random_pulser(rng: random.Random | None = None) -> Citizen:
    rng = rng or random
    pool = pulsers()
    return rng.choices(pool, weights=[c.freq for c in pool], k=1)[0]
