"""Бухгалтерия золота (faucet/sink ledger) — измерение потоков экономики.

Зачем: чтобы балансить экономику по ФАКТУ, а не на глаз (как делает EVE Online —
публикует ежемесячный баланс ISK по источникам и стокам). Накопитель живёт прямо
на игроке (`Player.econ` — JSONB {категория: суммарное золото}): хелпер пишет рядом
с каждым изменением золота, едет с объектом игрока и коммитится вместе с ним —
поэтому работает даже в чистых игровых функциях без доступа к сессии.

Знак = направление: КРАН (faucet) пишем плюсом, СТОК (sink) — минусом. Тогда
сумма по всем игрокам и категориям = чистый приток золота в мир за окно измерения
(World.econ_since). P2P-переводы (аукцион/торговля между игроками) НЕ логируем —
это перемещение, а не эмиссия/сжигание, иначе faucet/sink зашумится.
"""

# Категории и их человеко-подписи (для /econ). Порядок — для вывода.
FAUCETS: dict[str, str] = {
    "passive":   "🍺 Пассив (таверна)",
    "retail":    "🍻 Сбыт гостям",
    "hunt":      "🏹 Охота",
    "invasion":  "🪓 Орда орков",
    "raid":      "🐲 Рейд-боссы",
    "bonus":     "🎁 Бонусы/сундук",
    "referral":  "🤝 Рефералка",
    "nightrun":  "🌙 Ночная вылазка",
    "story":     "📜 Сюжет/события",
}
SINKS: dict[str, str] = {
    "upgrade":   "🏗 Апгрейд таверны",
    "building":  "🧱 Пристройки",
    "forge":     "⚒ Кузница (ковка)",
    "shop":      "🛒 Лавка (докупка сырья)",
    "skim":      "💸 Поборы города",
}
LABELS = {**FAUCETS, **SINKS}


def record(player, cat: str, delta) -> None:
    """Зачесть изменение золота в категорию (кран — плюс, сток — минус).
    Мутирует player.econ переприсваиванием (требование JSONB)."""
    d = int(delta or 0)
    if d == 0:
        return
    e = dict(getattr(player, "econ", None) or {})
    e[cat] = int(e.get(cat, 0)) + d
    player.econ = e
